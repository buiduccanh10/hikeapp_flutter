class Hike {
  int id;
  String hikeName;
  String description;
  String locationFrom;
  String locationTo;
  String date;
  String length;
  String duration;
  String is_parking;
  String level;

  Hike({
    required this.id,
    required this.hikeName,
    required this.description,
    required this.locationFrom,
    required this.locationTo,
    required this.date,
    required this.length,
    required this.duration,
    required this.is_parking,
    required this.level,
  });

}
